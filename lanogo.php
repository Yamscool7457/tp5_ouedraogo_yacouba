    </p><p class="text-historiq" ><strong>Le Burkina Faso regorge d'un riche patrimoine culturel et touristique, ce qui en fait une destination <br>
 privilégiée des touristes nationaux et internationaux. L'une des nombreuses attractions touristiques qui <br>
 forcent l'admiration des touristes est sans aucun doute les sculptures sur granite de Laongo.</strong></span></p>
 La création du site touristique est née de l'initiative d'artistes locaux qui étaient en quête <br>
 d’un lieu propice d’expression artistique.</span></p> <p>
Le projet a été présenté par le Comité national des arts plastiques du Burkina Faso en 1988 et <br>
 a été approuvé par les pouvoirs publics à la même date.</span></p>
 Le village de Laongo a été désigné comme le lieu idéal pour les créations artistiques. Le site <br> 
présente un relief accidenté couvert de blocs de roche en granite, idéal pour les sculptures de tous genres.</span></p>
Les œuvres d'art sont taillées directement sur la roche, ce qui leurs confèrent une originalité unique 
<br> d’où l'appellation du site "Les sculptures sur granite de Laongo".</span></p>
 Le site rassemble plusieurs œuvres d'art d'artistes nationaux et étrangers.</span></p>
 À ce jour, il existe plus de deux (200) cent types de créations artistiques impliquant différents <br>
 <br> thèmes de société.</span></p>
Chaque année, des artistes du monde entier sont invités à participer à un symposium qui a lieu sur <br>
 le site de Laongo. C’est l’occasion pour les participants d’exprimer leur créativité en laissant parler  <br>
leur imagination sur les blocs de roche.</span></p>
Ce site à lui seul est un véritable musée en plein air, qui plonge les visiteurs dans un environnement à la <br>
 fois sauvage, tandis que les sculptures offrent au site une petite touche de modernité. Les visiteurs se <br>
complairont devant les majestueuses sculptures à perte de vue présentes sur le site et qui sont soigneusement <br>
 taillés avec passion et précision.</span></p>
 Le site est facile d’accès par la voie terrestre à 35 km à l’Est de Ouagadougou dans <br>
 la ville de Ziniaré et à une dizaine 
 de kilomètres un peu plus loin dans le village de Laongo.</span></p>
</p>                                                
